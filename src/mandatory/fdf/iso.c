#include <math.h>
#include "map.h"

void    iso(int *p_3d, int *p_2d)
{
    p_2d[0] = (p_3d[0] - p_3d[1]) * cos(30 * M_PI / 180);
    p_2d[1] = (p_3d[0] + p_3d[1]) * sin(30 * M_PI / 180) - p_3d[2];
}
